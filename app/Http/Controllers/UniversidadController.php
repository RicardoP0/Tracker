    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'otros_univ'=>'required',
            'new_univ_name'=>'required|string',
        ]);
        $otros_univ = \App\Universidad::findOrFail($request->otros_univ);
        $otros_univ->nombre = $request->new_univ_name;
        $otros_univ->sede = "placeholder";
        $otros_univ->save();
        return redirect()->back();
    }

